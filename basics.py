# Creating a dictionary
my_dict = {
    "brand": "Ford",
    "model": "Mustang",
    "year": 1964
}

# Accessing values
print(my_dict["model"])  # Output: Ford

# Adding a new key-value pair
my_dict["color"] = "red"

# Modifying a value
my_dict["year"] = 2024

# Removing a key-value pair
del my_dict["model"]

